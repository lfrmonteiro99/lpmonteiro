export default function Skills() {
  const sectionRef = useRef(null);

  // Add subtle animation on scroll
  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry, index) => {
          if (entry.isIntersecting) {
            // Add a slight delay for each card
            setTimeout(() => {
              entry.target.classList.add("opacity-100");
              entry.target.classList.remove("translate-y-10");
            }, index * 100);
            observer.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.1 }
    );

    const cards = document.querySelectorAll(".skill-card");
    cards.forEach((card) => {
      observer.observe(card);
    });

    return () => cards.forEach((card) => observer.unobserve(card));
  }, []);

  return (
    <section
      id="skills"
      ref={sectionRef}
      className="sm:min-h-screen sm:mx-auto pt-25 relative"
    >
      {/* Subtle background pattern */}
      <div className="absolute inset-0 bg-grid-pattern opacity-5"></div>

      <div className="container mx-auto py-4 px-4 sm:px-8 md:px-12 mt-16 md:mt-20 relative z-10">
        <h1 className="text-4xl font-bold text-white mb-8 text-center relative text-white">
          {`<Skills />`}
        </h1>

        <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
          {allSkills.map((skillCategory, index) => (
            <div
              key={index}
              className="skill-card min-h-[200px] opacity-0 translate-y-10 transition-all duration-500 ease-out"
              style={{ transitionDelay: `${index * 150}ms` }}
            >
              <SkillCard
                category={skillCategory.category}
                skills={skillCategory.skills}
              />
            </div>
          ))}
        </div>
      </div>
    </section>
  );
}
